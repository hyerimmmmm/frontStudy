const x = -10; // 변수 x를 -10으로 초기화한다.

if (x > 0) { // 만약 x가 0보다 크면
    console.log("x는 양수입니다"); // "x는 양수입니다"를 콘솔에 출력한다.
} else if (x < 0) { // x가 0보다 크지 않고, 0보다 작다면
    console.log("x는 음수입니다"); // "x는 음수입니다"를 콘솔에 출력한다.
} else { // 위의 조건에도 해당되지 않으면
    console.log("x는 0입니다"); // "x는 0입니다"를 콘솔에 출력한다.
}