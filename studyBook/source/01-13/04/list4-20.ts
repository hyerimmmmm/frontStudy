const x = 3;
console.log(`x가 ${x}일 때`); // x가 3일 때
console.log(`x===3은 ${x === 3}`); // x===3은 true
console.log(`x!==3은 ${x !== 3}`); // x!==3이 false
console.log(`x<3은 ${x < 3}`); // x<3은 false
console.log(`x>3은 ${x > 3}`); // x>3은 false
console.log(`x<=3은 ${x <= 3}`); // x<=3은 true
console.log(`x>=3은 ${x >= 3}`); // x>=3은 true