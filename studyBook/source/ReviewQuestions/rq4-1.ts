// 변수 x에 10을 대입한다.
const x: number = 10;
// 변수 y에 2를 대입한다.
const y: number = 2;

// 더하기
const addition: number = x + y;
// 빼기
const subtraction: number = x - y;
// 곱하기
const multiplication: number = x * y;
// 나누기
const division: number = x / y;
// 나머지
const remainder: number = x % y;

// 콘솔에 결과를 출력한다.
console.log(`덧셈: ${addition}`); // 12
console.log(`뺄셈: ${subtraction}`); // 8
console.log(`곱셈: ${multiplication}`); // 20
console.log(`나눗셈: ${division}`); // 5
console.log(`나머지: ${remainder}`); // 0