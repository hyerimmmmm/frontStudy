// if 문
let color = "green";
if (color === "red") {
    console.log("빨강");
} else if (color === "blue") {
    console.log("파랑");
} else {
    console.log("기타");
}

// switch 문
color = "green";
switch (color) {
    case "red":
        console.log("빨강");
        break;
    case "blue":
        console.log("파랑");
        break;
    default:
        console.log("기타");
}