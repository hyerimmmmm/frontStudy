const age1: number = 25;
const age2: number = 30;

// 비교 연산자 <, >, <=, >=, ===, !==를 사용하여 비교한다.
console.log(`age1 < age2: ${age1 < age2}`);     // true
console.log(`age1 > age2: ${age1 > age2}`);     // false
console.log(`age1 <= age2: ${age1 <= age2}`);   // true
console.log(`age1 >= age2: ${age1 >= age2}`);   // false
console.log(`age1 === age2: ${age1 === age2}`); // false
console.log(`age1 !== age2: ${age1 !== age2}`); // true